public class ForLoopKarekok {
    public static void main(String[] args) {

        //1'den 5'e kadar olan sayıların karekokunu ekrana yazdır.
       for (int i = 1; i <=5; i++){
           System.out.println("karekok icinde "+ i*i+ "=  "+ i);
       }

    }
}
